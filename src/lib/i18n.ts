export function getStrings(lang: Language): TrackerStrings {
  return STRINGS[lang] ?? STRINGS.en;
}

export function detectLanguage(
  nav: Pick<Navigator, 'language' | 'languages'> | undefined = typeof navigator === 'undefined'
    ? undefined
    : navigator,
): Language {
  const languages = nav?.languages?.length ? nav.languages : [nav?.language ?? 'en'];
  return languages.some((language) => language.toLowerCase().startsWith('ru')) ? 'ru' : 'en';
}
